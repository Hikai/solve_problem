from z3 import *

x = BitVec("x", 8)
y = BitVec("y", 8)
a = BitVec("a", 8)
b = BitVec("b", 8)
s = Solver()
s.add((x & 1) + 5 + ((y >> 2) & 1) + 1 == 7, ((x >> 3) & 1) + 5 + ((y >> 3) & 1) + 1 == 6, ((x >> 1) & 1) + 5 + ((y >> 4) & 1) + 1 == 8, ((x >> 2) & 1) + 5 + (y & 1) + 1 == 7, ((x >> 4) & 1) + 5 + ((y >> 1) & 1) + 1 == 6, (a & 1) + 5 + ((b >> 2) & 1) + 1 == 7, ((a >> 3) & 1) + 5 + ((b >> 3) & 1) + 1 == 7, ((a >> 1) & 1) + 5 + ((b >> 4) & 1) + 1 == 7, ((a >> 2) & 1) + 5 + (b & 1) + 1 == 7, ((a >> 4) & 1) + 5 + ((b >> 1) & 1) + 1 == 6, 0x7a >= x, x >= 0x61, 0x7a >= y, y >= 0x61, 0x7a >= a, a >= 0x61, b == 0x70)
s.check()
print(s.model())
